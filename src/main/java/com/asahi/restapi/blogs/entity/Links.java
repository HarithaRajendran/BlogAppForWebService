package com.asahi.restapi.blogs.entity;

public class Links {
	
	private String url;
	private String rel;
	
	public Links() {
	}
	
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getRel() {
		return rel;
	}
	public void setRel(String rel) {
		this.rel = rel;
	}
}
